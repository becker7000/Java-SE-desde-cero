public class OperadoresIncremento {

    public static void main(String[] args) {

        int x=5;
        System.out.printf("\n\t x = %d",x++); //x = 5
        System.out.printf("\n\t x = %d",x); // x = 6

        /*
        *  Si usamos el operador ++ por la izquierda (prefijo)
        *  se hace un incremento inmediato.
        *
        *  Pero si usamos el operador ++ por la derecha (sufijo)
        *  se hace un incremento después de una asignación.
        * */

    }

}
